<p>
<b>BIO</b>: Professor Thomas is the current the Deputy Director of the Advanced 
Computing Research Centre, Director of the Mawson Institute SAR Visualisation 
Lab, and Director of the Wearable Computer Laboratory at the University of South 
Australia. He is currently a NICTA Fellow, Senior Member of the ACM, and 
visiting Scholar with the Human Interaction Technology Laboratory, University of 
Washington. His current research interests include: wearable computers, user 
interfaces, augmented reality, virtual reality, CSCW, and tabletop display 
interfaces.</p>
<p>ProfessorThomas' academic qualifications include the following: </p>
<p>1) B.A. in Physics, George Washington University; </p>
<p>2) M.S. in Computer Science, University of Virginia with a thesis titled:&nbsp; 
Pipeline Pyramids in Dynamic Scenes; and </p>
<p>3) Ph.D. in Computer Science, Flinders University with a thesis titled: 
Animating Direct Manipulation in Human Computer Interfaces</p>
<p>His experience includes working at the School of Information Technology and 
Mathematical Sciences, University of South Australia since 1990. He has run his 
own computer consultancy company. He was a Computer Scientist at the National 
Institute of Standards and Technology (A major US government laboratory for the 
Department of Commerce.), and a software engineer for the Computer Sciences 
Corporation and the General Electric Company.</p>
