<p><b>BIO</b>: Professor Peter Eades is a leading researcher in the area of 
diagram layout and aesthetics. Peter has invented many novel and well-used 
algorithms for complex diagramatic representation, particularly graph drawing. 
Peter has studied many aspects of graph layout from both mathematical 
underpinnings to layout and appearance aesthetics to diverse applications of 
graph drawing. Peter also has interests in various human-centric approaches to 
interaction with complex computing systems, including ambient and large screen 
displays.</p>
