<b>BIO</b>: Dr Helen Purchase is Senior Lecturer in the School of Computing 
Science at the University of Glasgow. She has worked in the area of empirical 
studies of graph layout for nearly 20 years, and also has research interests in 
visual aesthetics, task-based empirical design, collaborative learning in higher 
education, and sketch tools for design. She has recently published a&nbsp;practical 
guide&nbsp;to empirical studies&nbsp;entitled &quot;Experimental Human Computer Interaction&quot; (<a href="http://www.cambridge.org/au/academic/subjects/computer-science/computing-and-society/experimental-human-computer-interaction-practical-guide-visual-examples">Cambridge 
